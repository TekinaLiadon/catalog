<template>
    <el-input :value="modelValue" @input="updateInput" type="text"> </el-input>
</template>

<script>
    export default {
        name: "InputEl",
        props: {
            modelValue: [ String, Number ]
          },
        methods: {
            updateInput(event) {
                this.$emit("update:modelValue", event.target.value);
            }
        }
    }
</script>

